
package day5;

public class Car {

    private String model;
    private int year;
    private String color;

    public void setModel(String modelName) {
        model = modelName;
    }

    public String getModel() {
        return model;
    }

    public void setColor(String colorName) {
        color = colorName;
    }

    public String getColor() {
        return color;
    }

    public void setYear(int year1) {
        year = year1;
    }

    public int getYear(){
        return year;
    }
}